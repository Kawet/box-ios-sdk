- (void)viewDidLoad
{
    [super viewDidLoad];
	
	_textView.layer.borderColor = [[UIColor grayColor] CGColor];
	_textView.layer.borderWidth = 1;
	_textView.layer.cornerRadius = 8.;
	
    // Do any additional setup after loading the view from its nib.
}

